    public void ListaClientes() {
        rDao = new ResultadoDao();

        java.util.List<ResultadoTO> listarResultados = rDao.listarResultados();
        TablaDeRegistroDePuntajes.setAutoCreateRowSorter(true);
        model = (DefaultTableModel) TablaDeRegistroDePuntajes.getModel();
        Object[] ob = new Object[7];
        for (int i = 0; i < listarResultados.size(); i++) {
            ob[0] = listarResultados.get(i).getNombre_partida();
            ob[1] = listarResultados.get(i).getNombre_jugador1();
            ob[2] = listarResultados.get(i).getNombre_jugador2();
            ob[3] = listarResultados.get(i).getGanador();
            ob[4] = listarResultados.get(i).getPunto();
            ob[5] = listarResultados.get(i).getEstado();
            model.addRow(ob);
        }
        TablaDeRegistroDePuntajes.setModel(model);
    }

    private void limpiarTablaClientes() {
        for (int i = 0; i < TablaDeRegistroDePuntajes.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }
        TablaDeRegistroDePuntajes.setModel(model);
    }

    
    
