    private void jButton_IniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_IniciarActionPerformed

        
        
        if (jTextField_nombreJugador1.getText().equals("")
                || jTextField_nombreJugador2.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Ingrese los datos completos");

        } else {

            jButton_continuar.setVisible(true);
            jButton_reiniciar.setVisible(true);
            jButton_terminar.setVisible(true);
            
             jTextField_turno.setText(jTextField_nombreJugador1.getText());
            
            ResultadoTO res = new ResultadoTO();
            ResultadoDaoI re = new ResultadoDao();

            res.setNombre_jugador1(jTextField_nombreJugador1.getText());
            res.setNombre_jugador2(jTextField_nombreJugador2.getText());

            java.util.List<ResultadoTO> listarResultados = rDao.listarResultados();
            //Tener un row [FILA] ya establecida en la base de datos porque el listarResultados.size no tendra un tamaño
            for (int i = 0; i < listarResultados.size(); i++) {
                res.setNombre_partida("Partida " + (i + 1));
            }

            re.crearResultados(res);
            limpiarTablaClientes();
            ListaClientes();
            //capturarJugador();
            permisoInicio = "SI";
            jButton_Iniciar.setVisible(false);
            
            jTextField_nombreJugador1.setEditable(false);
            jTextField_nombreJugador2.setEditable(false);
            JOptionPane.showMessageDialog(null, "Se registro de manera satisfactoria ¡¡Que comienze el juego!!");
        }

    }//GEN-LAST:event_jButton_IniciarActionPerformed

